    private void jButtonPassarATaulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPassarATaulaActionPerformed
        // TODO add your handling code here:
        int posicioLlista = jListComandes.getSelectedIndex();
        
        if (posicioLlista != -1) {
            String dadesLlista = modelLlista.getElementAt(posicioLlista);
            
            String[] parts = dadesLlista.split(" \\| ");
            Object[] filaTaula = new Object[] { parts[0], parts[1], parts[2], parts[3] };
            
            modelTaula.addRow(filaTaula);
            modelLlista.remove(posicioLlista);
            
            int numElements = modelTaula.getRowCount();
            int total = 0;
            int i = 0;
            
            while (i < numElements) {
                String textQuant = modelTaula.getValueAt(i, 2).toString();
                int quantNum = Integer.parseInt(textQuant);
                total += quantNum;
                i++;
            }
            
            String estat = "Estat: " + numElements + " a la taula - Total unitats: " + total;
            jLabelEstat.setText(estat);
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un element de la llista.");
        }
    }//GEN-LAST:event_jButtonPassarATaulaActionPerformed

    private void jButtonEliminarFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarFilaActionPerformed
        // TODO add your handling code here:
        int filaTaula = jTableFinal.getSelectedRow();
        
        if (filaTaula != -1) {
            modelTaula.removeRow(filaTaula);
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una fila de la taula per eliminar");
        }
    }//GEN-LAST:event_jButtonEliminarFilaActionPerformed

    private void jButtonResumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonResumActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText(">>> COMANDES <<<\n");
        
        int i = 0;
        int totalFiles = modelTaula.getRowCount();
        
        while (i < totalFiles) {
            String cl = modelTaula.getValueAt(i, 0).toString();
            String pr = modelTaula.getValueAt(i, 1).toString();
            String qt = modelTaula.getValueAt(i, 2).toString();
            String ur = modelTaula.getValueAt(i, 3).toString();
    
            jTextArea1.append("Client: " + cl + " | Producte: " + qt + "x " + pr + " (" + ur + ")\n");
            i++;
        }
    }//GEN-LAST:event_jButtonResumActionPerformed

    private void jTableFinalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableFinalMouseClicked
        // TODO add your handling code here:
        int fila = jTableFinal.getSelectedRow();
        
        if (fila != -1) {
            String clientStr = modelTaula.getValueAt(fila, 0).toString();
            String producteStr = modelTaula.getValueAt(fila, 1).toString();
            String quantitatStr = modelTaula.getValueAt(fila, 2).toString();
            String urgentStr = modelTaula.getValueAt(fila, 3).toString();
            
            jTextFieldClient.setText(clientStr);
            jTextFieldQuantitat.setText(quantitatStr);
            jComboBoxProducte.setSelectedItem(producteStr);
            
            if (urgentStr.equals("Urgent")) {
                jCheckBoxUrgent.setSelected(true);
            } else {
                jCheckBoxUrgent.setSelected(false);
            }     
        }
    }//GEN-LAST:event_jTableFinalMouseClicked

