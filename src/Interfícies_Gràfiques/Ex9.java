    private void jButtonAfegirLlista1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAfegirLlista1ActionPerformed
        // TODO add your handling code here:
        String client = jTextFieldClient.getText().trim();
        String producte = jComboBoxProducte.getSelectedItem().toString();
        String quantitat = jTextFieldQuantitat.getText().trim();
        boolean esUrgent = jCheckBoxUrgent.isSelected();
        
        boolean dadesValides = true;
        String errors = "";
        
        if (client.isEmpty()) { dadesValides = false; errors += "- Client buit\n"; }
        if (quantitat.isEmpty()) { dadesValides = false; errors += "- Quantitat buida\n"; }
        
        if (dadesValides) {
            String urgent = esUrgent ? "Urgent" : "Normal";
            String linia = client + " | " + producte + " | " + quantitat + " | " + urgent;
            modelLlista.addElement(linia);
            
            jTextFieldClient.setText("");
            jTextFieldQuantitat.setText("");
            jCheckBoxUrgent.setSelected(false);
        } else {
            JOptionPane.showMessageDialog(this, errors, "Error de dades", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAfegirLlista1ActionPerformed

