    private void btnSeguentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguentActionPerformed
        String dni = txtDni.getText().trim();
        String pwd = new String(pwdPass.getPassword());

        if (dni.isEmpty() || pwd.length() < 4) {
            JOptionPane.showMessageDialog(this, "Error al introduïr les dades");
        } else {
            tabMenu.setSelectedIndex(1);
        }
    }//GEN-LAST:event_btnSeguentActionPerformed

    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        JFileChooser fc = new JFileChooser();
        File f = null;
        int seleccio = fc.showDialog(this, "Tria una imatge");

        if (seleccio == JFileChooser.APPROVE_OPTION) {
            f = fc.getSelectedFile();
            lblRuta.setText(f.getAbsolutePath());
        }
    }//GEN-LAST:event_btnFotoActionPerformed

