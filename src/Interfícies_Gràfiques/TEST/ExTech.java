    private void btnAfegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAfegirActionPerformed
        // TODO add your handling code here:
        String dept = txtDepartament.getText().trim();

        if (!dept.isEmpty()) {
            cmbDepartament.addItem(dept);
            cmbDepartament.setSelectedItem(dept);
        } else {
            JOptionPane.showConfirmDialog(this, "Introdueix un departament.");
        }

    }//GEN-LAST:event_btnAfegirActionPerformed

    private void btnAssignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignarActionPerformed
        // TODO add your handling code here:
        String nom = txtProjecte.getText().trim();
        String dept = cmbDepartament.getSelectedItem().toString();
        boolean dedicacio = rdbCompleta.isSelected();
        String ded = dedicacio ? "Jornada Completa" : "Mitja jornada";
        String extra = "";

        if (chkIdiomes.isSelected()) {
            extra += "Idiomes ";
        }
        if (chkNocturn.isSelected()) {
            extra += "Nocturnitat ";
        }
        if (chkPerill.isSelected()) {
            extra += "Perillositat ";
        }

        if (nom.isEmpty() || dept.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Dades buides.");
        } else {
            String linia = "PROJECTE: " + nom + " - " + dept + " - " + ded + " - " + extra;
            modelLlista.addElement(linia);
            tabMenu.setSelectedIndex(2);
        }
    }//GEN-LAST:event_btnAssignarActionPerformed

